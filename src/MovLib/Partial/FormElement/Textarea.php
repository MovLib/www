class Textarea extends \MovLib\Partial\FormElement\InputText {

  /**
   * Get the textarea form element.
   *
   * @return string
   *   The textarea form element.
   */
  public function __toString() {
    // @devStart
    // @codeCoverageIgnoreStart
    try {
    // @codeCoverageIgnoreEnd
    // @devEnd
    $this->attributes["id"] = $this->id;
    $this->attributes["name"] = $this->id;
    $this->attributes["aria-multiline"] = "true";
    return
      "<fieldset>" .
        "<legend>{$this->label}{$this->required}{$this->helpPopup}{$this->helpText}</legend>" .
        "<textarea{$this->presenter->expandTagAttributes($this->attributes)}>{$this->value}</textarea>" .
      "</fieldset>"
    ;
    // @devStart
    // @codeCoverageIgnoreStart

    } catch (\Exception $e) {
      return $this->calloutError("<pre>{$e}</pre>", "Stacktrace");
    }
    // @codeCoverageIgnoreEnd
    // @devEnd
  }

}
